#include "push_swap.h"

int	ft_len_tabs(char **strs)
{
    int    i;
    int    l;

    i = 0;
    l = 0;
    while (strs[i])
    {
        l += ft_strlen(strs[i]);
        i++;
    }
    return (l);
}
